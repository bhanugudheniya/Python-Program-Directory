# Area Of Circle - PI(r)^2
# --------------
print("Area Of Circle")
PI = 3.14
radiusCircleArea = int(input("Enter Radius of Circle\n"))
Area_Of_Circle = PI * radiusCircleArea * radiusCircleArea
print("Area of Circle : ", Area_Of_Circle)

#******************************************************************#

# Area od Rectangle - length * width
# -----------------
print("\nArea of Rectangle")
length = int(input("Enter Length of Rectangle\n"))
width = int(input("Enter Width of Rectangle\n"))
Area_Of_Rectangle = length * width
print("Area of Rectangle : ", Area_Of_Rectangle)

#******************************************************************#

# Circumference Of Circle - 2*PI*r
# -----------------------
print("\nCircumference Of Circle")
radiusCircleCircumference = int(input("Enter Radius of Circle\n"))
Circumference_Of_Circle = 2 * PI * radiusCircleCircumference
print("Circumference of Circle : ", Circumference_Of_Circle)

#******************************************************************#

# Area of Square - a^2
# --------------------
print("\nArea of Square")
areaSquare = int(input("Enter Area of Square\n"))
Area_Of_Square = areaSquare * areaSquare
print("Area of Square : ", Area_Of_Square)