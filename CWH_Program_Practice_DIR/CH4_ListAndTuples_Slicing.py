# List
friends = ['Bhanu', 'Ashish', 'Shobit', 'Naveen']
print(friends)

# Tuple
CharChillar = ('Bhanu', 'Ashish', 'Shobit', 'Naveen')
print(CharChillar)

# Slicing
f1 = friends[0:2]
print(f1)       # ['Bhanu', 'Ashish']

f1 = friends[-1:4]
print(f1)       # ['Naveen']

# ------ #

c1 = CharChillar[0:2]
print(c1)       # ('Bhanu', 'Ashish')

c1 = CharChillar[-1:4]
print(c1)       # ('Naveen',)